from pymol import cmd

def combine_pdb_mol2(pdb_codes):
    for pdb_code in pdb_codes:
        protein_file = f"./{pdb_code}/{pdb_code}_protein.pdb"
        ligand_file = f"./{pdb_code}/{pdb_code}_ligand.mol2"
        output_file = f"./combined_ligand_protein_pdb_files/{pdb_code}_PDBBind_combined.pdb"

        # Clear the previous session
        cmd.reinitialize()

        # Load the protein and ligand
        cmd.load(protein_file, "protein")
        cmd.load(ligand_file, "ligand")

        # Combine into a single object
        cmd.create("complex", "protein or ligand")

        # Save to a new file
        cmd.save(output_file, "complex")

        print(f"Saved combined structure to {output_file}")

